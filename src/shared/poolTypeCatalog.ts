function slug(input: string): string {
  return input.trim().toLowerCase().replace(/[^a-z0-9]+/g, "_").replace(/^_+|_+$/g, "");
}

function def(
  input: Omit<PoolTypeDefinition, "commissioner_options" | "rule_variants"> & {
    commissioner_options?: string[];
    rule_variants?: PoolTypeRuleVariant[];
  },
): PoolTypeDefinition {
  return {
    ...input,
    commissioner_options: Array.from(new Set([...(input.commissioner_options || []), ...BASE_COMMISSIONER_OPTIONS])),
    rule_variants: input.rule_variants && input.rule_variants.length > 0
      ? input.rule_variants
      : [{ key: "standard", label: "Standard" }],
  };
}

