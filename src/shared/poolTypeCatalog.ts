export function validatePoolTypeRules(input: {
  sportKey: string;
  formatKey: string;
  rules?: Record<string, unknown> | null;
}): string[] {
  const errors: string[] = [];
  const definition = getPoolTypeByKey(input.formatKey);
  if (!definition && !isSupportedPoolTypeKey(input.formatKey)) {
    errors.push(`Unsupported format key: ${input.formatKey}`);
    return errors;
  }
  if (definition && definition.sport !== "multi_sport" && definition.sport !== input.sportKey) {
    errors.push(`Format ${definition.key} does not support sport ${input.sportKey}`);
  }
  const rules = (input.rules || {}) as Record<string, unknown>;
  if (definition?.template === "bundle_pool") {
    const childPools = Array.isArray(rules.bundleChildPools) ? rules.bundleChildPools : [];
    if (childPools.length === 0) {
      errors.push("Bundle pools require at least one child pool in rules.bundleChildPools");
    }
  }
  if (definition?.template === "confidence" || definition?.template === "ats_confidence") {
    const confidenceMode = typeof rules.confidenceMode === "string" ? rules.confidenceMode : "";
    if (!confidenceMode && !rules.pointsPerWin) {
      errors.push("Confidence pools require confidenceMode or pointsPerWin configuration");
    }
  }
  const maxEntries = Number(rules.maxEntries ?? 0);
  if (maxEntries < 0 || !Number.isFinite(maxEntries)) {
    errors.push("rules.maxEntries must be a non-negative number when provided");
  }
  if (rules.payoutStructure !== undefined && !Array.isArray(rules.payoutStructure)) {
    errors.push("rules.payoutStructure must be an array when provided");
  }
  return errors;
}
